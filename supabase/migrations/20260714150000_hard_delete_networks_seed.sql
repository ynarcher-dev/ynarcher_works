-- [정리] NETWORKS 데모 시드 하드 딜리트(물리 삭제)
-- 목적: 20260705240000_seed_dummy_data.sql 가 적재한 NETWORKS 마스터 데모 데이터
--       (스타트업/전문가/협력사)와, 그 마스터를 FK로 참조하는 시드 데이터를 물리 삭제한다.
