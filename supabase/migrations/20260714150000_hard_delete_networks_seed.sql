-- 결정: 통상 규약(물리삭제 금지·soft delete)의 예외로, "안 쓰는 하드코딩 데모 데이터"를
--       완전 제거하려는 사용자 명시 요청에 따라 물리 삭제(delete)로 수행한다.
-- 삭제 순서: FK(ON DELETE 기본=RESTRICT) 위반을 피하기 위해 자식→부모 순으로 지운다.
-- 블라스트 반경(주의): 시드 스타트업을 참조하는 FUND 투자/M&A 딜/AC 멘토링 데모 시드도 함께 삭제된다.
--   보존: 임직원(users e0..)·부서(departments d0..)는 실데이터가 참조할 수 있어 삭제하지 않는다.
--   보존: 펀드/프로그램/프로젝트/결재 등 시드 스타트업을 직접 참조하지 않는 시드는 유지한다.
-- 대상: 고정 시드 UUID만 정확히 지정하여 실업로드 데이터는 건드리지 않는다.
-- 보안 게이트: 데이터 DELETE 전용(신규 테이블/RPC/Storage 정책/SECURITY DEFINER 없음) → 해당 없음.
-- =====================================================================

