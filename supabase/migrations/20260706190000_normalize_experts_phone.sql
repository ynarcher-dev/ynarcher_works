-- =====================================================================
-- [Phase 15] 전문가 연락처(phone) 기존 데이터 정규화
-- - 하이픈/공백 등 숫자 외 문자를 제거해 숫자만 저장하도록 통일한다.
--   (신규 입력은 상세 폼에서 이미 숫자만 저장 — ExpertForm)
-- - 숫자 외 문자가 포함된 미삭제 행만 대상으로 하여 멱등 실행되게 한다.
-- - guest_invitations.phone은 OTP/초대 매칭 형식에 영향 가능성이 있어 제외한다.
-- 근거: 마스터 목록/상세 마스킹 표시(maskPhone)와의 일관성.
-- =====================================================================

update public.experts
set phone = regexp_replace(phone, '\D', '', 'g')
where phone is not null
  and phone ~ '\D';
